class ArrayDemo{

	
	public static void main(String[] args){

		int marks[]	= new int[3];	
		marks[0] = 100;
		marks[1] = 700;
		marks[2] = 900;
				
		
		System.out.println(" Marks lentgh :: "+marks.length);
		
		System.out.println(" Value at Index 0 :: "+marks[0]);
		
		System.out.println(" Value at Index 1 :: "+marks[1]);
		
		System.out.println(" Value at Index 2 :: "+marks[2]);
		
		int temp[] = {200,300,500,800};
		
		System.out.println(" temp lentgh :: "+temp.length);
		
		System.out.println(" Value at Index0 for TEMP  :: "+temp[0]);
		
		System.out.println(" Value at Index1 for Temp :: "+temp[1]);
		
		System.out.println(" Value at Index2 for Temp :: "+temp[2]);
		
		System.out.println(" Value at Index3 for Temp :: "+temp[3]);
		
		
		
		
	}
}